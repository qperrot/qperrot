#include "libft.h"

int		ft_atoi(const char *str)
{
	long	nbr;
	int		i;
	int		neg;

	i = 0;
	nbr = 0;
	neg = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n'
			|| str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		nbr += str[i] - 48;
		nbr *= 10;
		i++;
	}
	nbr /= 10;
	nbr *= neg;
	return ((int)nbr);
}
