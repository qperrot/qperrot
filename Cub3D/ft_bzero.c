#include <stdlib.h>
#include <stdio.h>
#include <string.h>
#include <unistd.h>

void	ft_bzero(void *s, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((char*)s)[i] = 0;
		i++;
	}
	return ;
}
